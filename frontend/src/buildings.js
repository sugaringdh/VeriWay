/**
 * 건물명으로 좌표 검색
 * 공백·괄호 무시, 부분 일치 지원
 */
export function getBuildingCoords(name) {
  if (!name) return null
  const clean = name.trim().replace(/\s+/g, '').replace(/[()（）]/g, '')

  // 1) 정확히 일치 (공백 제거 후)
  for (const [key, val] of Object.entries(BUILDINGS)) {
    if (key.replace(/\s+/g, '') === clean) return val
  }

  // 2) 포함 관계 (긴 키 우선 → "새힘관엘리베이터"가 "새힘관"보다 먼저)
  const sorted = Object.entries(BUILDINGS).sort((a, b) => b[0].length - a[0].length)
  for (const [key, val] of sorted) {
    const cleanKey = key.replace(/\s+/g, '')
    if (clean.includes(cleanKey) || cleanKey.includes(clean)) return val
  }

  return null
}

export default BUILDINGS
